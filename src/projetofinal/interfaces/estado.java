    public estado(Integer idEstado) {
        initComponents();
        carregarControles();
        try {
            estado = estadoControle.obterEstado(idEstado);
            nome.setText(estado.getNome());
            uf.setText(estado.getUf().toString());
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        carregaCombo();
    }
    
    public void carregarControles() {
        estado = new Estado();
        estadoControle = new EstadoControle();
        paisControle = new PaisControle();
    }

    public void carregaCombo() {
        try {
            idPais.addItem(new ComboItem(null, "-- Selecione --"));
            List<Pais> paiss = paisControle.obtemTodosPaiss();
            for (Pais pais : paiss) {
                ComboItem comboItem = new ComboItem(pais.getId(), pais.getNome());
                idPais.addItem(comboItem);
                if (estado.getPais_id() != null && estado.getPais_id().equals(pais.getId())) {
                    idPais.setSelectedItem(comboItem);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    private boolean validaCampos() {
        if (nome.getText() == null || nome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O campo 'Nome' deve ser preenchido!");
            return false;
        }
        if (uf.getText() == null || uf.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O campo 'UF' deve ser preenchido!");
            return false;
        }
        if (uf.getText().length() > 3) {
            JOptionPane.showMessageDialog(null, "O campo 'UF' deve ser preenchido com no máximom 3 caracteres!");
            return false;
        }
        if (idPais.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "O campo 'País' deve ser preenchido!");
            return false;
        }
        return true;
    }
    
