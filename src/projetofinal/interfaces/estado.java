    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed
        // TODO add your handling code here:
        if (!validaCampos()) {
            return;
        }
        
        estado.setNome(nome.getText());
        estado.setUf(uf.getText());
        ComboItem itemPais = (ComboItem) idPais.getSelectedItem();
        estado.setPais_id(itemPais.getKey());
        estadoControle.gravar(estado);
        
        JOptionPane.showMessageDialog(null, "Estado cadastrada com sucesso!");
        
        setVisible(false);
        new estadoLista().setVisible(true);
    }//GEN-LAST:event_salvarActionPerformed

